## Converson de Temperatura
#####
xvar = input('Digite algo: ')

print('O tipo primitivo desse valor é :',type(xvar))
print('Só tem espaços? ', xvar.isspace())
print('É um número? ', xvar.isnumeric())
print('É alfabético? ', xvar.isalpha())
print('Está em maiúsculo? ', xvar.isupper())
print('Está em minúsculo? ', xvar.islower())
print('Está capitalizada? ', xvar.capitalize())
print('É imprimivel ?',xvar.isprintable())

