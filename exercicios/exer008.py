## Conversor de Medidas
#####
print('Conversor de metro para centímetro.')
vm = float(input('Digite um valor em metros: '))
cmc = vm * 100

print('Converter: {} metros é {} centímetros.'.format(vm,cmc))

vc = float(input('Digite um valor em centímetros: '))
cmm = vc / 100

print('Converter: {} centímetro é {} metros.'.format(vc,cmm))


