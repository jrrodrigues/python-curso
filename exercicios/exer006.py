## Dobro, Triplo e Raiz
#####
import math

nro = int(input('Digite um número: '))

nrD = nro*2
nrT = nro*3
nrR = math.sqrt(nro)

print('O dobro de {} é {} '.format(nro, nrD))
print('O triplo de {} é {} '.format(nro, nrT))
print('A raiz  de {} é {} '.format(nro, nrR))
